#include "philo.h"

int check_is_valid(char **av)
{
    int i = 0;
    int j = 1;
    while (av[j])
    {
        i = 0;
        while (av[j][i] && av[j][i] == ' ')
            i++;
        if (av[j][i] == '\0')
            return (1);
        if (av[j][i] == '+' || av[j][i] == '-')
             i++;
        while (av[j][i] && (av[j][i] >= '0' && av[j][i] <= '9'))
            i++;
        if (av[j][i] != '\0')
            return (1);    
        j++;       
    }
    return (0);
}
